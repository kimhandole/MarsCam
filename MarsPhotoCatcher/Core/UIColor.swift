import UIKit

extension UIColor {
    //Background Color
    public class var Background: UIColor {
        return UIColor(red: 20/256, green: 38/256, blue: 66/256, alpha: 1.0)
    }
    
    public class var Accent: UIColor {
        return UIColor(red: 8/256, green: 24/256, blue: 51/256, alpha: 1.0)
    }
    
    public class var Highlight: UIColor {
        return UIColor(red: 0.98, green: 0.23, blue: 0.36, alpha: 1.0)
    }
}
